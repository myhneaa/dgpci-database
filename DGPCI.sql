    ID_PERSOANA NUMBER(11),
    COD_PERSOANA CHAR(13) NOT NULL,
    TIP_PERSOANA CHAR(9) NOT NULL,
    NUME VARCHAR2(100) NOT NULL,
    PRENUME VARCHAR2(100) NOT NULL,
    ADRESA VARCHAR2(100) NOT NULL,
    LOCALITATE VARCHAR2(30) NOT NULL,
    JUDET VARCHAR2(10) NOT NULL,
    DATA_NASTERE_INFIINTARE DATE NOT NULL,
    
    CONSTRAINT PK_PERSOANA PRIMARY KEY (ID_PERSOANA),
    CONSTRAINT UK_COD_PERS UNIQUE (COD_PERSOANA),
    
    CONSTRAINT CK_COD_LEN CHECK (LENGTH(TRIM(COD_PERSOANA)) BETWEEN 2 AND 13),
    CONSTRAINT CK_TIP_PERS CHECK (TIP_PERSOANA IN ('FIZICA', 'JURIDICA')),
    CONSTRAINT CK_JUDET CHECK (JUDET IN (
        'ALBA', 'ARAD', 'ARGES', 'BACAU', 'BIHOR', 'BISTRITA-NASAUD', 'BOTOSANI', 
        'BRAILA', 'BRASOV', 'BUCURESTI', 'BUZAU', 'CALARASI', 'CARAS-SEVERIN', 
        'CLUJ', 'CONSTANTA', 'COVASNA', 'DAMBOVITA', 'DOLJ', 'GALATI', 'GIURGIU', 
        'GORJ', 'HARGHITA', 'HUNEDOARA', 'IALOMITA', 'IASI', 'ILFOV', 'MARAMURES', 
        'MEHEDINTI', 'MURES', 'NEAMT', 'OLT', 'PRAHOVA', 'SALAJ', 'SATU MARE', 
        'SIBIU', 'SUCEAVA', 'TELEORMAN', 'TIMIS', 'TULCEA', 'VASLUI', 'VALCEA', 'VRANCEA'
    ))
);

-- PERMIS
CREATE TABLE PERMIS (
    ID_PERMIS NUMBER(16),
    NUMAR_PERMIS CHAR(9) NOT NULL,
    DATA_EMITERE DATE NOT NULL,
    DATA_EXPIRARE DATE NOT NULL,
    UNITATE_EMITENTA VARCHAR2(100) NOT NULL,
    ID_PERSOANA NUMBER(11) NOT NULL,
    
    CONSTRAINT PK_PERMIS PRIMARY KEY (ID_PERMIS),
    CONSTRAINT UK_NR_PERMIS UNIQUE (NUMAR_PERMIS),
    
    CONSTRAINT FK_PERMIS_PERS FOREIGN KEY (ID_PERSOANA) 
        REFERENCES PERSOANA(ID_PERSOANA) ON DELETE CASCADE,
        
    CONSTRAINT CK_NR_POZITIV CHECK (NUMAR_PERMIS > 0),
    CONSTRAINT CK_PERMIS_DATA CHECK (DATA_EMITERE < DATA_EXPIRARE)
);

-- CATEGORIE
CREATE TABLE CATEGORIE (
    COD_CATEGORIE VARCHAR2(3),
    DESCRIERE VARCHAR2(30) NOT NULL,
    VARSTA_MINIMA NUMBER(2) DEFAULT 18 NOT NULL,
    TERMEN_VALABILITATE NUMBER(2) DEFAULT 10 NOT NULL,
    
    CONSTRAINT PK_CATEGORIE PRIMARY KEY (COD_CATEGORIE)
);

-- SANCTIUNE
CREATE TABLE SANCTIUNE (
    ID_SANCTIUNE NUMBER(15),
    TIP_SANCTIUNE VARCHAR2(10) NOT NULL,
    PUNCTE_PENALIZARE NUMBER(2) NOT NULL,
    DATA_EXPIRARE DATE NOT NULL,
    ID_PERSOANA NUMBER(11) NOT NULL,
    
    CONSTRAINT PK_SANCTIUNE PRIMARY KEY (ID_SANCTIUNE),
    
    CONSTRAINT FK_SANCT_PERS FOREIGN KEY (ID_PERSOANA) 
        REFERENCES PERSOANA(ID_PERSOANA) ON DELETE CASCADE,
        
    CONSTRAINT CK_TIP_SANCT CHECK (TIP_SANCTIUNE IN ('AVERTISMENT', 'AMENDA', 'SUSPENDARE')),
    CONSTRAINT CK_PUNCTE CHECK (PUNCTE_PENALIZARE BETWEEN 0 AND 15)
);

-- VEHICUL
CREATE TABLE VEHICUL (
    SERIE_SASIU CHAR(17),
    MARCA VARCHAR2(20) NOT NULL,
    MODEL VARCHAR2(50) NOT NULL,
    AN_FABRICATIE NUMBER(4) NOT NULL,
    PRIMA_INMATRICULARE DATE,
    CAPACITATE_CILINDRICA NUMBER(5) NOT NULL,
    PUTERE NUMBER(5) NOT NULL,
    COMBUSTIBIL VARCHAR2(20) NOT NULL,
    NORMA_POLUARE CHAR(8) NOT NULL,
    CULOARE VARCHAR2(12) NOT NULL,
    
    CONSTRAINT PK_VEHICUL PRIMARY KEY (SERIE_SASIU),
    
    CONSTRAINT CK_AN_FABR CHECK (AN_FABRICATIE >= 1886),
    CONSTRAINT CK_CAPACITATE CHECK (CAPACITATE_CILINDRICA BETWEEN 0 AND 99999),
    CONSTRAINT CK_PUTERE CHECK (PUTERE BETWEEN 1 AND 9999),
    CONSTRAINT CK_COMBUSTIBIL CHECK (COMBUSTIBIL IN ('BENZINA', 'MOTORINA', 'BENZINA HIBRID', 'MOTORINA HIBRID', 'NEPOLUANT')),
    CONSTRAINT CK_NORMA CHECK (NORMA_POLUARE IN ('NON-EURO', 'EURO1', 'EURO2', 'EURO3', 'EURO4', 'EURO5', 'EURO6', 'EURO7'))
);

-- INMATRICULARE
CREATE TABLE INMATRICULARE (
    ID_INMATRICULARE NUMBER(16),
    TIP_INMATRICULARE VARCHAR2(20) NOT NULL,
    NUMAR_INMATRICULARE VARCHAR2(10) NOT NULL,
    DATA_INMATRICULARE DATE NOT NULL,
    DATA_RADIERE DATE,
    SERIE_SASIU CHAR(17) NOT NULL,
    
    CONSTRAINT PK_INMATRICULARE PRIMARY KEY (ID_INMATRICULARE),
    CONSTRAINT UK_NR_INMAT UNIQUE (NUMAR_INMATRICULARE),
    
    CONSTRAINT FK_INM_VEH FOREIGN KEY (SERIE_SASIU) 
        REFERENCES VEHICUL(SERIE_SASIU),
        
    CONSTRAINT CK_TIP_INM CHECK (TIP_INMATRICULARE IN ('PERMANENT', 'PROVIZORIU', 'TEMPORAR', 'PROBE', 'DIPLOMATIC', 'MINISTER'))
);

-- RCA
CREATE TABLE RCA (
    ID_RCA NUMBER(15),
    ASIGURATOR VARCHAR2(30) NOT NULL,
    DECONTARE_DIRECTA NUMBER(1) NOT NULL,
    DATA_INCEPUT DATE NOT NULL,
    DATA_SFARSIT DATE NOT NULL,
    SERIE_SASIU CHAR(17) NOT NULL,
    
    CONSTRAINT PK_RCA PRIMARY KEY (ID_RCA),
    
    CONSTRAINT FK_RCA_VEH FOREIGN KEY (SERIE_SASIU) 
        REFERENCES VEHICUL(SERIE_SASIU),
        
    CONSTRAINT CK_DECONTARE CHECK (DECONTARE_DIRECTA IN (0, 1)),
    CONSTRAINT CK_RCA_DATA CHECK (DATA_INCEPUT < DATA_SFARSIT)
);

-- ITP
CREATE TABLE ITP (
    ID_ITP NUMBER(15),
    VERDICT VARCHAR2(16) NOT NULL,
    DATA_VERIFICARE DATE NOT NULL,
    DATA_EXPIRARE DATE NOT NULL,
    UNITATE_INSPECTIE VARCHAR2(50) NOT NULL,
KILOMETRAJ NUMBER(7) NOT NULL,
    SERIE_SASIU CHAR(17) NOT NULL,
    
    CONSTRAINT PK_ITP PRIMARY KEY (ID_ITP),
    
    CONSTRAINT FK_ITP_VEH FOREIGN KEY (SERIE_SASIU) 
        REFERENCES VEHICUL(SERIE_SASIU),
        
    CONSTRAINT CK_VERDICT CHECK (VERDICT IN ('ADMIS', 'ADMIS CU DEFECTE', 'RESPINS')),
    CONSTRAINT CK_ITP_DATA CHECK (DATA_VERIFICARE <= DATA_EXPIRARE),
CONSTRAINT CK_ITP_KILOMETRAJ CHECK (KILOMETRAJ >= 0)
);

-- DETALII_PERMIS
CREATE TABLE DETALII_PERMIS (
    ID_PERMIS NUMBER(16),
    COD_CATEGORIE VARCHAR2(3),
    DATA_OBTINERE DATE DEFAULT SYSDATE NOT NULL,
    DATA_EXPIRARE DATE NOT NULL,
    CONDITIE VARCHAR2(50),
    
    CONSTRAINT PK_DETALII PRIMARY KEY (ID_PERMIS, COD_CATEGORIE),
    
    CONSTRAINT FK_DET_PERMIS FOREIGN KEY (ID_PERMIS) 
        REFERENCES PERMIS(ID_PERMIS) ON DELETE CASCADE,
        
    CONSTRAINT FK_DET_CATEG FOREIGN KEY (COD_CATEGORIE) 
        REFERENCES CATEGORIE(COD_CATEGORIE),
        
    CONSTRAINT CK_DP_DATA CHECK (DATA_OBTINERE < DATA_EXPIRARE)
);

-- ISTORIC_INSTRAINARE
CREATE TABLE ISTORIC_INSTRAINARE (
    ID_INSTRAINARE NUMBER(15),
    ID_PERSOANA NUMBER(11) NOT NULL,
    SERIE_SASIU CHAR(17) NOT NULL,
    DATA_INSTRAINARE DATE DEFAULT SYSDATE NOT NULL,
    PRET_VANZARE NUMBER(10, 2) DEFAULT 0 NOT NULL,
    KILOMETRAJ NUMBER(7) DEFAULT 0 NOT NULL,
    
    CONSTRAINT PK_INSTRAINARE PRIMARY KEY (ID_INSTRAINARE),
    
    CONSTRAINT FK_INST_PERS FOREIGN KEY (ID_PERSOANA) 
        REFERENCES PERSOANA(ID_PERSOANA),
        
    CONSTRAINT FK_INST_VEH FOREIGN KEY (SERIE_SASIU) 
        REFERENCES VEHICUL(SERIE_SASIU),

    CONSTRAINT CK_INST_VEH_KILOMETRAJ CHECK (KILOMETRAJ >= 0)
);

